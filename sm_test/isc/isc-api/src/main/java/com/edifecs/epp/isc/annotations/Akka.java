package com.edifecs.epp.isc.annotations;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marks a command handler interface (annotated with {@link CommandHandler}) or
 * a command method as accessible or inaccessible through Akka messaging.
 * 
 * @author c-adamnels
 */
@Documented
@Target({ ElementType.METHOD, ElementType.TYPE })
@Retention(RetentionPolicy.RUNTIME)
public @interface Akka {
    boolean enabled();
}
